// Helper functions
export const getCurrentDate = () => new Date();

export const isUpcomingEvent = (eventDate) => {
  return new Date(eventDate) >= getCurrentDate();
};

export const getUpcomingEvents = () => {
  return eventsData
    .filter(event => isUpcomingEvent(event.date))
    .sort((a, b) => new Date(a.date) - new Date(b.date));
};

export const getPreviousEvents = () => {
  return eventsData
    .filter(event => !isUpcomingEvent(event.date))
    .sort((a, b) => new Date(b.date) - new Date(a.date));
};

export const getFlagshipEvents = () => {
  return eventsData.filter(event => event.isFlagship);
};

export const getEventById = (id) => {
  return eventsData.find(event => event.id === parseInt(id));
};